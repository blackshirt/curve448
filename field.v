module curve448

import math.bits
import math.unsigned

// Field is an an opaque represents the field of 448-bits integer of GF modulo p = 2⁴⁴⁸ - 2²²⁴ - 1.
// Its represented as unsaturated (redundant) limb in the form of 8 of 56-bits limbs, ie:
//     t.e0*2⁰ + t.e1*2⁵⁶ + t.e2*2¹¹² + t.e3*2¹⁶⁸ + t.e4*2²²⁴ + t.e5*2²⁸⁰ + t.e6*2³³⁶ + t.e7*2³⁹²
//
@[noinit]
struct Field {
mut:
	// Between operations, all limbs are expected to be lower than 2⁵⁷ (ie, fits in 56-bits)
	el [8]u64
}

// new_field creates an empty field
@[inline]
fn new_field() Field {
	return fe_zero
}

// The size of field limb, in bits
const fe_limb_size = 56
// Masking value for field's limb value, ie, 0x00ff_ffff_ffff_ffff
const fe_masklow_56bits = u64(0x00ff_ffff_ffff_ffff)

// zero field element
const fe_zero = Field{
	el: [u64(0), 0, 0, 0, 0, 0, 0, 0]!
}

// one field element
const fe_one = Field{
	el: [u64(1), 0, 0, 0, 0, 0, 0, 0]!
}

// fe_p is a prime modulus of the field, ie, p = 2⁴⁴⁸ - 2²²⁴ - 1
const fe_p = Field{
	el: [u64(0x00FF_FFFF_FFFF_FFFF), u64(0x00FF_FFFF_FFFF_FFFF), u64(0x00FF_FFFF_FFFF_FFFF),
		u64(0x00FF_FFFF_FFFF_FFFF), u64(0x00FF_FFFF_FFFF_FFFE), u64(0x00FF_FFFF_FFFF_FFFF),
		u64(0x00FF_FFFF_FFFF_FFFF), u64(0x00FF_FFFF_FFFF_FFFF)]!
}

// fe_clear overwrites a field element in-place. It is intended for best-effort
// source-level wiping of temporary values that may contain secret-dependent data
// after use. Strict secure-zeroization guarantees require auditing generated
// C/assembly or replacing this with a compiler-resistant wipe primitive.
@[direct_array_access; inline]
fn fe_clear(mut z Field) {
	for i := 0; i < 8; i++ {
		z.el[i] = 0
	}
}

// fe_add performs modular field addition: z = a + b (mod p).
// It adds the elements limb-by-limb, extracts the carries, and performs a single
// reduction step using the Solinas prime identity: 2^448 = 2^224 + 1 (mod p).
@[direct_array_access; inline]
fn fe_add(mut z Field, a Field, b Field) {
	// c holds the carry bits extracted from each 56-bit limb addition.
	// Since we are adding two 56-bit (or slightly larger) limbs, the sum can exceed
	// 56 bits. The carry bit c[i] will be either 0 or 1.
	mut c := [8]u64{}
	// Operations like fe_add(mut a, a, b) corrupt outputs because z.el[i] overwrites
	// input limbs during early loop iterations.Write to an isolated local
	// struct res before copying to destination z
	mut res := Field{}

	// Step 1: Add limbs individually.
	// We iterate through all 8 limbs. For each limb, we compute the sum of the
	// limbs of a and b, extract the carry (bits above the 56-bit boundary),
	// and mask the limb to retain only the lower 56 bits.
	for i := 0; i < 8; i++ {
		res.el[i] = a.el[i] + b.el[i]
		c[i] = res.el[i] >> fe_limb_size
		res.el[i] = (res.el[i] & fe_masklow_56bits)
	}

	// Step 2: Apply modular reduction for Curve448.
	// The Curve448 modulus is p = 2^448 - 2^224 - 1, which implies the identity:
	//     2^448 = 2^224 + 1 (mod p)
	// Any overflow carry from the highest limb (limb 7, representing 2^392)
	// corresponds to a carry of 2^448. By the identity, this carry is reduced to:
	//     c[7] * 2^224 + c[7] * 1
	// Therefore, we add c[7] to the 0-th limb (representing 2^0) and the 4th limb
	// (representing 2^224).
	res.el[0] += c[7]
	res.el[4] += c[7]

	// Step 3: Propagate the remaining carries to the next higher limbs.
	// For each limb from 1 to 7, we add the carry bit generated by the addition
	// of the immediately preceding limb (c[i-1]).
	for i := 1; i < 8; i++ {
		res.el[i] += c[i - 1]
	}
	fe_carry_propagates(mut res)
	fe_clone(mut z, res)
}

// fe_sub performs modular field subtraction: z = a - b (mod p).
// To prevent underflow, it first adds 2 * p (a multiple of the modulus) to a,
// subtracts b, extracts the carries, and applies a reduction step.
@[direct_array_access; inline]
fn fe_sub(mut z Field, a Field, b Field) {
	// c holds the carry/borrow bits extracted from each limb subtraction.
	mut c := [8]u64{}

	// Step 1: Perform subtraction with biased limbs.
	// Since a.el[i] could be smaller than b.el[i], a direct subtraction could underflow
	// and wrap around. To guarantee that the result remains positive and fits in u64,
	// we add 2 * p.el[i] (which is at least 2^57 - 4) to a.el[i] before subtracting b.el[i].
	// We then extract the carry (which acts as a borrow flag) and mask the limb.
	for i := 0; i < 8; i++ {
		// add by 2 * p.el[i]
		z.el[i] = (a.el[i] + (fe_p.el[i] << 1)) - b.el[i]
		c[i] = z.el[i] >> fe_limb_size
		z.el[i] = z.el[i] & fe_masklow_56bits
	}

	// Step 2: Apply the modular reduction.
	// Just like in addition, any carry from the highest limb (c[7]) represents
	// 2^448. We reduce it by adding c[7] to limb 0 (representing 2^0) and limb 4
	// (representing 2^224).
	z.el[0] += c[7]
	z.el[4] += c[7]

	// Step 3: Propagate the carries.
	// Add the carry bit from the previous limb to the current limb.
	for i := 1; i < 8; i++ {
		z.el[i] += c[i - 1]
	}

	fe_carry_propagates(mut z)
}

// fe_negate negates a field element: z = -a (mod p).
// It subtracts each limb of a from the corresponding limb of 2 * p,
// propagates the carries, and applies modular reduction.
@[direct_array_access; inline]
fn fe_negate(mut z Field, a Field) {
	// Step 1: Subtract each limb of a from 2 * p.
	// The limbs of 2 * p are:
	//     2 * p.el[i] = 2^57 - 2 = 0x01fffffffffffffe  (for i != 4)
	//     2 * p.el[4] = 2^57 - 4 = 0x01fffffffffffffc  (for i == 4)
	// This subtraction is guaranteed not to underflow because a.el[i] < 2^57.
	z.el[0] = u64(0x01fffffffffffffe) - a.el[0]
	z.el[1] = u64(0x01fffffffffffffe) - a.el[1]
	z.el[2] = u64(0x01fffffffffffffe) - a.el[2]
	z.el[3] = u64(0x01fffffffffffffe) - a.el[3]
	z.el[4] = u64(0x01fffffffffffffc) - a.el[4]
	z.el[5] = u64(0x01fffffffffffffe) - a.el[5]
	z.el[6] = u64(0x01fffffffffffffe) - a.el[6]
	z.el[7] = u64(0x01fffffffffffffe) - a.el[7]

	// Step 2: Extract and propagate the carries.
	mut c := [8]u64{}
	for i := 0; i < 8; i++ {
		c[i] = z.el[i] >> fe_limb_size
		z.el[i] = (z.el[i] & fe_masklow_56bits)
	}

	// Step 3: Apply modular reduction using the Solinas prime identity:
	//     2^448 = 2^224 + 1 (mod p)
	z.el[0] += c[7]
	z.el[4] += c[7]

	// Step 4: Propagate the carries to higher limbs.
	for i := 1; i < 8; i++ {
		z.el[i] += c[i - 1]
	}

	// final carry
	fe_carry_propagates(mut z)
}

// fe_clone clones x into z
@[direct_array_access; inline]
fn fe_clone(mut z Field, x Field) {
	for i := 0; i < 8; i++ {
		z.el[i] = x.el[i]
	}
}

